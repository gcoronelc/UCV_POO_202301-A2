	private void setEditStatus(boolean activo){
		txtCapital.setEnabled(activo);
		txtTasa.setEnabled(activo);
		txtPeriodos.setEnabled(activo);
		btnProcesar.setEnabled(activo);
		btnNuevo.setEnabled(!activo);
		if(activo){
			txtCapital.requestFocus();
		}
	}
	
	
