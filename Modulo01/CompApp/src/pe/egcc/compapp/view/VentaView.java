  private void btnProcesarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProcesarActionPerformed
    try {
      // Datos
      String tipo = this.cboTipo.getSelectedItem().toString();
      double total = Double.parseDouble(txtTotal.getText());
      // Proceso
      ItemDto[] repo = control.procesar(tipo, total);
      // Mostrar reporte
      mostrarReporte(repo);
    } catch (Exception e) {
      String msg = "Error en el proceso.\n" 
              + (e.getMessage()==null?"":e.getMessage());
      JOptionPane.showMessageDialog(rootPane, msg,
              "ERROR", JOptionPane.ERROR_MESSAGE);
    }
  }//GEN-LAST:event_btnProcesarActionPerformed

  private void mostrarReporte(ItemDto[] repo) {
    DefaultTableModel tabla;
    tabla = (DefaultTableModel) tblRepo.getModel();
    tabla.setRowCount(0); // Elimina todas las filas
    // Recorrido tipo colecci√≥n de un arreglo
    for (ItemDto dto : repo) {
      Object[] rowData = {dto.getConcepto(), dto.getImporte()};
      tabla.addRow(rowData);
    }
  }

