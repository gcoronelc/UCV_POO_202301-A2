  private void llenarCombo() {
    this.cboTipo.removeAllItems();
    String[] tipos = control.obtenerTipos();
    // Recorrido indexado de un arreglo
    for (int i = 0; i < tipos.length; i++) {
      this.cboTipo.addItem(tipos[i]);
    }
    this.cboTipo.setSelectedIndex(-1);
  }

